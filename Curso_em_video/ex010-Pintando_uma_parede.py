Largura = float(input("Digite um valor em largura da parede: "))
Altura = float(input("Digite um valor em altura da parede: "))
Area = Largura * Altura
Litros = Area / 2

print(f"Sua parede tem a dimensão de {Largura} X {Altura} e sua área é de {Area}m^2")
print(f"Para pintar essa parede, você precisará de {Litros}l de tinta")
